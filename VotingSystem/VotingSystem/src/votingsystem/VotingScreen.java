    private void lockVotingUI() {
        // Disable all radio buttons inside candidatesPanel
        for (java.awt.Component comp : candidatesPanel.getComponents()) {
            disableRadiosRecursive(comp);
        }
        btnSubmit.setEnabled(false);
}

    private void disableRadiosRecursive(java.awt.Component comp) {
        if (comp instanceof javax.swing.JRadioButton rb) {
            rb.setEnabled(false);
        } else if (comp instanceof java.awt.Container container) {
            for (java.awt.Component child : container.getComponents()) {
                disableRadiosRecursive(child);
            }
        }
}
    
    private boolean hasAlreadyVoted() {
    String sql = "SELECT COUNT(*) AS total FROM votes WHERE user_id = ?";

    try (java.sql.Connection conn = DB.connect();
         java.sql.PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt(1, userId);

        try (java.sql.ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                return rs.getInt("total") > 0;
            }
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "DB error: " + e.getMessage());
    }

    return false;
}
    
    private String getMyVotesSummary() {
    String sql = """
        SELECT v.position, c.name
        FROM votes v
        JOIN candidates c ON v.candidate_id = c.id
        WHERE v.user_id = ?
        ORDER BY c.position_order
    """;


    StringBuilder sb = new StringBuilder();

    try (java.sql.Connection conn = DB.connect();
         java.sql.PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt(1, userId);

        try (java.sql.ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                sb.append(rs.getString("position"))
                  .append(": ")
                  .append(rs.getString("name"))
                  .append("\n");
            }
        }

    } catch (Exception e) {
        return "Error reading votes: " + e.getMessage();
    }

    return sb.length() == 0 ? "No votes found." : sb.toString();
}



