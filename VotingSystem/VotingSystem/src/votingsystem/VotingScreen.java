    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // 1) Validate: must choose 1 candidate per position
        for (var entry : groupByPosition.entrySet()) {
            String position = entry.getKey();
            javax.swing.ButtonGroup group = entry.getValue();

            if (group.getSelection() == null) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Please select a candidate for: " + position);
                return;
            }
        }

        // 2) Confirm submit
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "Submit your votes now? You may not be able to change after submitting.",
            "Confirm Submit",
            javax.swing.JOptionPane.YES_NO_OPTION
        );
        if (confirm != javax.swing.JOptionPane.YES_OPTION) return;

        String insert = "INSERT INTO votes(user_id, position, candidate_id) VALUES(?,?,?)";

        // 3) Save in one transaction
        try (java.sql.Connection conn = DB.connect();
            java.sql.PreparedStatement ps = conn.prepareStatement(insert)) {

            conn.setAutoCommit(false);

            for (var entry : groupByPosition.entrySet()) {
                String position = entry.getKey();
                javax.swing.ButtonGroup group = entry.getValue();

                int candidateId = Integer.parseInt(group.getSelection().getActionCommand());

                ps.setInt(1, userId);
                ps.setString(2, position);
                ps.setInt(3, candidateId);
                ps.addBatch();
            }

            ps.executeBatch();
            conn.commit();

            javax.swing.JOptionPane.showMessageDialog(this, "Votes submitted successfully!");

            // 4) Lock UI after submit
        lockVotingUI();

        } catch (java.sql.SQLException e) {
            // UNIQUE(user_id, position) triggers if user already voted
            String msg = (e.getMessage() != null && e.getMessage().toLowerCase().contains("unique"))
            ? "You already voted. Submission blocked."
            : "Database error: " + e.getMessage();
            javax.swing.JOptionPane.showMessageDialog(this, msg);

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

