private void loadCandidates() {

    candidatesPanel.removeAll();
    candidatesPanel.setLayout(new javax.swing.BoxLayout(candidatesPanel, javax.swing.BoxLayout.Y_AXIS));
    groupByPosition.clear();

    String sql = """
        SELECT id, name, position
        FROM candidates
        ORDER BY position_order, name
    """;

    String currentPosition = null;
    javax.swing.JPanel currentRow = null;

    try (java.sql.Connection conn = DB.connect();
         java.sql.PreparedStatement ps = conn.prepareStatement(sql);
         java.sql.ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            int candidateId = rs.getInt("id");
            String name = rs.getString("name");
            String position = rs.getString("position");

            // new position section
            if (currentPosition == null || !currentPosition.equals(position)) {
                currentPosition = position;

                javax.swing.JLabel header = new javax.swing.JLabel(position);
                header.setFont(header.getFont().deriveFont(java.awt.Font.BOLD, 14f));
                header.setBorder(javax.swing.BorderFactory.createEmptyBorder(12, 0, 6, 0));
                header.setHorizontalAlignment(javax.swing.SwingConstants.LEFT);

                // wrapper that forces left placement
                javax.swing.JPanel headerWrap = new javax.swing.JPanel(new java.awt.BorderLayout());
                headerWrap.setOpaque(false);
                headerWrap.add(header, java.awt.BorderLayout.WEST);

                candidatesPanel.add(headerWrap);


                currentRow = new javax.swing.JPanel(new java.awt.FlowLayout(java.awt.FlowLayout.LEFT,10,5));
                candidatesPanel.add(currentRow);

                groupByPosition.put(position, new javax.swing.ButtonGroup());
            }

            // candidate card
            javax.swing.JPanel card = new javax.swing.JPanel(new java.awt.BorderLayout(8, 0));
            card.setBorder(javax.swing.BorderFactory.createCompoundBorder(
                    javax.swing.BorderFactory.createLineBorder(java.awt.Color.GRAY),
                    javax.swing.BorderFactory.createEmptyBorder(8, 10, 8, 10)
            ));

            javax.swing.JLabel nameLabel = new javax.swing.JLabel(name);

            javax.swing.JRadioButton radio = new javax.swing.JRadioButton();
            radio.setActionCommand(String.valueOf(candidateId)); // IMPORTANT
            groupByPosition.get(position).add(radio);

            card.add(nameLabel, java.awt.BorderLayout.CENTER);
            card.add(radio, java.awt.BorderLayout.EAST);

            currentRow.add(card);
        }

        candidatesPanel.revalidate();
        candidatesPanel.repaint();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Load error: " + e.getMessage());
    }
}





